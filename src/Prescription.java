    public void autoID()
    {
        try {
            Statement s = con1.createStatement();
            rs = s.executeQuery("select MAX(preid) from prescription");
            rs.next();
            rs.getString("MAX(preid)");
            
            if(rs.getString("MAX(preid)")==null)
            {
                lbpno.setText("PC001");
            }
            else
            {
                long id = Long.parseLong(rs.getString("MAX(preid)").substring(2,rs.getString("MAX(preid)").length()));
                id++;
                lbpno.setText("PC"+String.format("%03d",id));
            }
            
            
        } catch (SQLException ex) {
            Logger.getLogger(Patient.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
   
