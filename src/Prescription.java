    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String preno = lbpno.getText();
        String chno = txtchno.getText();
   
        String desctype = txtdetype.getText();
        String desc = txtdesc.getText();
        
        Connect();
        
        try {
            String query = "insert into prescription(preid,channelid,doctorname,detype,description) values(?,?,?,?,?)";
            pst = con1.prepareStatement(query);
            pst.setString(1,preno);
            pst.setString(2,chno);
            pst.setString(3,newdocname);
            pst.setString(4,desctype);
            pst.setString(5,desc);
            pst.executeUpdate();
            
            JOptionPane.showMessageDialog(this,"Prescription Created!!!!!!!!!");
            
            autoID();
            
            txtchno.setText("");
            txtdetype.setText("");
            txtdesc.setText("");
            txtchno.requestFocus();
            
        } catch (SQLException ex) {
            Logger.getLogger(Patient.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

