    public void table_row()
    {
        try {
             
            String query = "select channel.channelno,doctor1.name,patient1.name,channel.roomno,channel.date from channel inner join doctor1 on channel.doctorname = doctor1.doctorno inner join patient1 on channel.patientname = patient1.patientno where doctor1.log_id=?";
            pst = con1.prepareStatement(query);
            pst.setInt(1,newid);
            rs = pst.executeQuery();
            ResultSetMetaData rsm = rs.getMetaData();
            int c;
            c = rsm.getColumnCount();
            
            DefaultTableModel df = (DefaultTableModel)jTable1.getModel();
            df.setRowCount(0);
            
            while(rs.next())
            {
                Vector v2 = new Vector();
                
                for(int i=1;i<=c;i++)
                {
                v2.add(rs.getString(1));
                v2.add(rs.getString(2));
                v2.add(rs.getString(3));
                v2.add(rs.getString(4));
                v2.add(rs.getString(5));
                
                }
                
                df.addRow(v2);
            }
            
            
            
            
        } catch (SQLException ex) {
            Logger.getLogger(Patient.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
